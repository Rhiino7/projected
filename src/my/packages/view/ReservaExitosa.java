    public ReservaExitosa(Empresa empresa, DBconnection connection, String name, Usuario user, String evento) {
        this.user = user;
        this.empresa = empresa;
        this.connection = connection;
        this.name = name;
        this.nEvento = evento;
        
        for (Evento evento1 : empresa.getEventos()) {
            if(evento1.getNombre().equals(nEvento)){
                this.evento = evento1;
            }
        }
        
        empresa.reservar(user, this.evento);
        initComponents();
        this.setLocationRelativeTo(null);
    }

